    private fun getmangagenre(document: Document): String {
        var es = document.select("#comicdetail div.listdetail_box.ldb2 div.ld_boxs div")[2].select("div.ld_body a")
        var genre = ""
        for (i in 0 until es.size) {
            genre = genre + es[i].text() + ", "
            if (i == es.size - 1) {
                genre += es[i].text()
            } else {
                genre = genre + es[i].text() + ", "
            }
        }
        return genre
    }

