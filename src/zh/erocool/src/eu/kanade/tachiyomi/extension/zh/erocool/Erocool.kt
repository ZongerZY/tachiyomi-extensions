    override fun searchMangaParse(response: Response): MangasPage {
        val body = response.body()!!.string()
        val document = Jsoup.parseBodyFragment(body)
        var mangasElements = document.select("div.list-wrapper a.list-wrap.gallery")
        var mangas = ArrayList<SManga>(mangasElements.size)
        for (mangaElement in mangasElements) {
            mangas.add(SManga.create().apply {
                title = mangaElement.select("a div.list-header h3").text()
                thumbnail_url = mangaElement.select("a div.list-content").attr("style").replace("background-image:url(", "").replace(");", "")
                url = mangaElement.select("a.list-wrap.gallery").attr("href")
            })
        }
        return MangasPage(mangas, mangasElements.size == 45)
    }

    override fun searchMangaRequest(page: Int, query: String, filters: FilterList): Request {
        if (query != "") {
            return myGet("$mainUrl/search/q_$query/popular/page/$page")
        } else {
            val RankStr = filters.map { if (it is RankFilter) { it.toUriPart() } else "" }.filter { it != "" }.joinToString("")
            val TagsStr = filters.map { if (it is TagsFilter) { it.toUriPart() } else "" }.filter { it != "" }.joinToString("")
            val ParodiesStr = filters.map { if (it is ParodiesFilter) { it.toUriPart() } else "" }.filter { it != "" }.joinToString("")
            val CharactersStr = filters.map { if (it is CharactersFilter) { it.toUriPart() } else "" }.filter { it != "" }.joinToString("")
            val AuthorStr = filters.map { if (it is AuthorFilter) { it.toUriPart() } else "" }.filter { it != "" }.joinToString("")
            if (TagsStr == "" && ParodiesStr == "" && CharactersStr == "" && AuthorStr == "") {
                if (RankStr == "popular")
                    return myGet("$mainUrl/rank/history/page/$page")
                else if (RankStr == "")
                    return myGet("$mainUrl/latest/page/$page")
                else
                    return myGet("$mainUrl/language/$RankStr/page/$page")
            } else {
                var nullNum = 0
                if (TagsStr != "") {
                    nullNum ++
                }
                if (ParodiesStr != "") {
                    nullNum ++
                }
                if (CharactersStr != "") {
                    nullNum ++
                }
                if (AuthorStr != "") {
                    nullNum ++
                }
                if (nullNum != 1) {
                    throw UnsupportedOperationException("按类型检索时\n{标签} {原作} {人物} {作者}\n只能选择一项\n其它项请调至为第一个\n[默认]")
                }
                if (TagsStr != "") {
                    return myGet("${mainUrl}${TagsStr}$RankStr/page/$page")
                }
                if (ParodiesStr != "") {
                    return myGet("${mainUrl}${ParodiesStr}$RankStr/page/$page")
                }
                if (CharactersStr != "") {
                    return myGet("${mainUrl}${CharactersStr}$RankStr/page/$page")
                }
                if (AuthorStr != "") {
                    return myGet("${mainUrl}${AuthorStr}$RankStr/page/$page")
                }
            }
            return myGet("$mainUrl/rank/day/page/$page")
        }
    }

